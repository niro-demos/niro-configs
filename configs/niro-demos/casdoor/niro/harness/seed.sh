  - credential_id: GLOBAL_ADMIN
    description: "Built-in Casdoor global admin, created automatically by
      Casdoor's own first-boot bootstrap (object.InitDb()), owner org
      \"built-in\". Effective privilege: user.IsGlobalAdmin() in
      object/user.go returns true for ANY user whose Owner == \"built-in\",
      regardless of the IsAdmin flag — this account also has IsAdmin=true so
      it is unambiguously the top-level admin. Global admin can manage every
      organization, user, application, cert, provider, role, permission,
      group, and record in the instance. Login: POST $BASE_URL/api/login,
      JSON {\"username\":\"admin\",\"password\":\"...\",\"organization\":
      \"built-in\",\"application\":\"app-built-in\",\"type\":\"login\"}
      (type required). Use to verify standard users and the Acme org-admin
      are denied instance-wide admin surfaces (org management, other orgs'
      users, /api/get-organizations, provider/cert management)."
    type: username_password
    identifier: admin
    secret: "$ADMIN_PASSWORD"

  - credential_id: ACME_STANDARD_A
    description: "Standard non-admin user \"alice\" in org \"acme\" (a
      dedicated test tenant seeded by niro/harness/init_data.seed.json, owner
      != \"built-in\" so IsGlobalAdmin() is false and IsAdmin=false — no
      elevated capability of any kind). Owns her own Casdoor user profile
      (acme/alice) only. Login: POST $BASE_URL/api/login with JSON body
      {\"username\":\"alice\",\"password\":\"...\",\"organization\":\"acme\",
      \"application\":\"app-acme\",\"type\":\"login\"} (type required).
      Pair with ACME_STANDARD_B for
      horizontal-escalation tests: authenticate as alice, attempt to
      read/modify bob's profile (acme/bob) via /api/get-user,
      /api/update-user, /api/get-user-application etc., expect 403/owner
      mismatch. Pair with GLOBAL_ADMIN and ACME_ORG_ADMIN for
      vertical-escalation tests against org-management and cross-org
      endpoints."
    type: username_password
    identifier: alice
    secret: "$ALICE_PASSWORD"

  - credential_id: ACME_STANDARD_B
    description: "Standard non-admin user \"bob\" in org \"acme\", same shape
      as ACME_STANDARD_A (alice) but a DIFFERENT principal owning a
      different user profile (acme/bob) so cross-user access attempts from
      alice have something real to fail at. Login: same shape as
      ACME_STANDARD_A with username=\"bob\"."
    type: username_password
    identifier: bob
    secret: "$BOB_PASSWORD"

  - credential_id: ACME_ORG_ADMIN
    description: "Org-scoped admin \"acme-admin\", Owner=\"acme\" (NOT
      \"built-in\"), IsAdmin=true. Per object/user.go IsApplicationAdmin(): an
      admin whose Owner equals the application's Organization can administer
      that application/org (acme, app-acme) — e.g. manage acme's own users,
      roles, permissions. Per IsGlobalAdmin() (Owner==\"built-in\" only) this
      account is NOT a global admin: it should be denied instance-wide
      surfaces (managing org \"built-in\", other organizations, global
      provider/cert config, /api/get-organizations across tenants). Login:
      POST $BASE_URL/api/login with JSON body {\"username\":\"acme-admin\",
      \"password\":\"...\",\"organization\":\"acme\",\"application\":
      \"app-acme\",\"type\":\"login\"} (type required). Pair with GLOBAL_ADMIN to verify org-admin privileges
      stop at the acme org boundary (org-isolation / tenant-boundary
      testing) — this is the primary account for that class of test."
    type: username_password
    identifier: acme-admin
    secret: "$ACME_ADMIN_PASSWORD"

  - credential_id: ACME_APP_OAUTH_CLIENT
    description: "OAuth2 client_id/client_secret pair for application
      app-acme (org acme), auto-generated by Casdoor on seed
      (object.AddApplication) and read back from the sqlite DB by seed.sh —
      rotates on every reset/reseed. Use for client_credentials /
      authorization_code / token endpoint testing at $BASE_URL/api/login/
      oauth/access_token and $BASE_URL/api/login/oauth/authorize. Distinct
      code path from human username_password logins above — use to probe
      whether the service/client-credential path silently inherits
      human-admin privileges or bypasses org scoping. identifier is the
      OAuth client_id; secret is the OAuth client_secret."
    type: username_password
    identifier: "$APP_ACME_CLIENT_ID"
    secret: "$APP_ACME_CLIENT_SECRET"
EOF

cat > "$FIXTURES_FILE" <<EOF
