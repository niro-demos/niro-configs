#!/usr/bin/env bash
# Ensure the deterministic baseline is up and generate ../credentials.yaml and
# ../fixtures.yaml from it.
#
# Seeding itself happens inside the app: main.go calls object.InitFromFile()
# on every boot, which reads init_data.seed.json (via the initDataFile env var
# start.sh sets) and reconciles the "acme" org/app/users to match it exactly
# (delete + recreate when they already differ). initDataNewOnly=false makes
# this authoritative, not additive. The always-present built-in org/admin
# come from Casdoor's own object.InitDb() bootstrap on first-ever boot
# (admin/123 in org "built-in").
#
# So seed.sh's own job is: (1) make sure the server is running (start.sh is a
# no-op if it's already healthy), (2) read back the one value that is
# generated randomly at seed time (the OAuth clientId/clientSecret for
# app-acme) from the sqlite DB, and (3) render credentials.yaml/fixtures.yaml.
set -euo pipefail
HARNESS_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=lib.sh
source "$HARNESS_DIR/lib.sh"

"$HARNESS_DIR/start.sh"

if ! command -v sqlite3 >/dev/null 2>&1; then
  echo "[seed] FATAL: sqlite3 CLI not found, cannot read back app-acme's generated OAuth client credentials" >&2
  exit 1
fi

# Give the very first post-boot InitFromFile() pass a moment to finish writing
# if start.sh's health check raced it (health comes up before InitFromFile
# completes in rare cold-start cases); retry the read a few times.
read_app_acme_client() {
  sqlite3 -separator '|' "$DB_FILE" \
    "select client_id, client_secret from application where owner='admin' and name='app-acme' limit 1;" 2>/dev/null
}

CLIENT_ROW=""
for _ in $(seq 1 10); do
  CLIENT_ROW="$(read_app_acme_client || true)"
  if [ -n "$CLIENT_ROW" ]; then
    break
  fi
  sleep 1
done

if [ -z "$CLIENT_ROW" ]; then
  echo "[seed] FATAL: app-acme row not found in $DB_FILE after seeding" >&2
  exit 1
fi

APP_ACME_CLIENT_ID="${CLIENT_ROW%%|*}"
APP_ACME_CLIENT_SECRET="${CLIENT_ROW##*|}"

read_user_id() {
  # $1=owner $2=name
  sqlite3 "$DB_FILE" "select id from user where owner='$1' and name='$2' limit 1;" 2>/dev/null
}

ADMIN_ID="$(read_user_id built-in admin || true)"
ALICE_ID="$(read_user_id acme alice || true)"
BOB_ID="$(read_user_id acme bob || true)"
ACME_ADMIN_ID="$(read_user_id acme acme-admin || true)"

cat > "$CREDENTIALS_FILE" <<EOF
