        password: password
        role: user

  - name: guestbook_entry
    description: "Seeded guestbook comment used by the Stored XSS and SQLi
      modules. The guestbook table has one row: comment_id=1,
      comment='This is a test comment.', name='test'."
    value:
      comment_id: 1
      comment: "This is a test comment."
      name: test

  - name: vulnerability_modules
    description: "DVWA vulnerability modules reachable after login. Each is at
      /vulnerabilities/<name>/. These are the exercisable attack surfaces at
      security level 'low'."
    value:
      - sqli
      - sqli_blind
      - xss_r
      - xss_s
