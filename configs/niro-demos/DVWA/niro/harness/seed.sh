  - name: db_config
    description: "Database connection details from config.inc.php (env-driven).
      MariaDB 10 running in the 'db' service; DVWA connects as the dvwa user."
    value:
      db_server: db
      db_database: dvwa
      db_user: dvwa
      db_port: 3306
EOF
echo "[seed] wrote $NIRO_DIR/fixtures.yaml"

echo "[seed] done"
