  - name: target_url
    description: "Base URL of the running DVWA instance. All vulnerability
      modules and API routes are under this root."
    value: "$BASE_URL"

  - name: security_level
    description: "DVWA security level set via DEFAULT_SECURITY_LEVEL env var.
      'low' exposes the full vulnerability catalog with minimal input
      sanitization. Can be changed per-session via the 'security' cookie
      (values: low, medium, high, impossible)."
    value: low

  - name: seeded_users
    description: "Users created by setup.php. All have MD5-hashed passwords.
      admin has role='admin'; the rest have role='user'. User IDs are 1-5."
    value:
